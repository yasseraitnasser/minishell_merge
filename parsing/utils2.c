#include "../minishell.h"

void	copy_whats_between_quotes(char **str, char c, char *new, int *i)
{
	(*str)++;
	while (**str && **str != c)
	{
		new[*i] = **str;
		(*i)++;
		(*str)++;
	}
	(*str)++;
}
